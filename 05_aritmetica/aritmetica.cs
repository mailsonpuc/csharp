using System;

namespace MyApplication
{
  class Program
  {
    static void Main(string[] args)
    {


      int num1 = 10, num2 = 5;

      //-----------------------------------------------
      //soma
      Console.WriteLine(num1 + num2);
      //-----------------------------------------------
      


      //-----------------------------------------------
      //subtrair
       Console.WriteLine(num1 - num2);
      //-----------------------------------------------
      

       //-----------------------------------------------
      //multiplica
       Console.WriteLine(num1 * num2);
     //-----------------------------------------------
     


       //-----------------------------------------------
      //divisao
       Console.WriteLine(num1 / num2);
      //-----------------------------------------------
      



        //-----------------------------------------------
      //modulo
       Console.WriteLine(num1 % num2);
      //-----------------------------------------------
      



        //-----------------------------------------------
      //increment
       Console.WriteLine(num1++);
      //-----------------------------------------------
      



        //-----------------------------------------------
      //derement
       Console.WriteLine(num1 --);
      //-----------------------------------------------
      







    }
  }
}
